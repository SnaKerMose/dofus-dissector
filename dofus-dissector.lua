local msg_type_field = ProtoField.uint16("dofus.msg_type", "Message Type", base.DEC, msg_types_table, 0xFFFC)
local msg_number_field = ProtoField.uint32("dofus.msg_number", "Message Number", base.DEC)
local msg_len_field = ProtoField.uint24("dofus.msg_len", "Message Length", base.DEC)
local msg_body = ProtoField.bytes("dofus.msg_body", "Message Body")

dofus_protocol.fields = {
  msg_type_field,
  msg_number_field,
  msg_len_field,
  msg_body
}

-- buf: packet's buffer (https://www.wireshark.org/docs/wsdg_html_chunked/lua_module_Tvb.html#lua_class_Tvb)
-- pinfo: packet information (https://www.wireshark.org/docs/wsdg_html_chunked/lua_module_Pinfo.html#lua_class_Pinfo)
-- tree: packet details view (https://www.wireshark.org/docs/wsdg_html_chunked/lua_module_Tree.html#lua_class_TreeItem)
function dofus_protocol.dissector(buf, pinfo, tree)
  if buf:len() == 0 then
    return
  end

  local protocol_tree = tree:add(dofus_protocol, buf(), "Dofus Protocol")

  -- packet was cut-off
  if buf:len() ~= buf:reported_len() then
    return
  end

  local msg_offset = 0 -- offset of the message in the TCP payload
  local msg_count = 0 -- number of messages
  while msg_offset < buf:len() do
    -- Truncated message (https://wiki.wireshark.org/Lua/Dissectors)
    if msg_offset + 2 > buf:len() then
        pinfo.desegment_len = DESEGMENT_ONE_MORE_SEGMENT
        pinfo.desegment_offset = msg_offset
        return
    end

    local msg_tree = protocol_tree:add(dofus_protocol, buf(msg_offset), "Dofus Message")

    msg_tree:add(msg_type_field, buf(msg_offset, 2))

    local msg_len_size = bit.band(buf(msg_offset + 1, 1):uint(), 0x03) -- size of the length is the two last bits of the second byte

    local msg_number_size = 0
    if is_packet_outgoing(pinfo) then
      msg_number_size = 4
      msg_tree:add(msg_number_field, buf(msg_offset + 2, msg_number_size))
    end

    -- Truncated message (https://wiki.wireshark.org/Lua/Dissectors)
    if msg_offset + 2 + msg_number_size + msg_len_size > buf:len() then
        pinfo.desegment_len = DESEGMENT_ONE_MORE_SEGMENT
        pinfo.desegment_offset = msg_offset
        return
    end

    local msg_len = 0
    if msg_len_size > 0 then
        local msg_len_buf = buf(msg_offset + 2 + msg_number_size, msg_len_size)
        msg_len = msg_len_buf:uint()
        msg_tree:add(msg_len_field, msg_len_buf, msg_len)
    end

    local msg_full_len = 2 + msg_number_size + msg_len_size + msg_len
    msg_tree:set_len(msg_full_len)

    -- Truncated message (https://wiki.wireshark.org/Lua/Dissectors)
    local msg_missing_bytes = msg_offset + msg_full_len - buf:len()
    if msg_missing_bytes > 0 then
        pinfo.desegment_len = DESEGMENT_ONE_MORE_SEGMENT
        pinfo.desegment_offset = msg_offset
        return
    end

    if msg_len > 0 then
        local msg_body_offset = msg_offset + msg_full_len - msg_len
        msg_tree:add(msg_body, buf(msg_body_offset, math.min(msg_len, buf:len() - msg_body_offset)))
    end

    msg_offset = msg_offset + msg_full_len
    msg_count = msg_count + 1
  end

  pinfo.cols.protocol = dofus_protocol.name -- set protocol column
  pinfo.cols.info = get_packet_direction(pinfo) .. " Msg=" .. msg_count .. " Len=" .. buf:len()

  assert(msg_offset == buf:len(), "Message was larger than packet (" .. msg_offset .. "/" .. buf:len() .. ")")
end

function get_packet_direction(pinfo)
    if is_packet_outgoing(pinfo) then
      return "Client → Server"
    else
      return "Server → Client"
    end
end

function is_packet_outgoing(pinfo)
  return pinfo.dst_port == dofus_protocol_port
end

local tcp_port = DissectorTable.get("tcp.port")
tcp_port:add(dofus_protocol_port, dofus_protocol)
